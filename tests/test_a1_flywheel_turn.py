from __future__ import annotations

import json
from pathlib import Path

import pytest

from tools import a1_flywheel_turn as turn


def _write(path: Path, value: str) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(value, encoding="utf-8")
    return path


def test_shared_teacher_lineage_rejects_stale_current_and_wrong_predecessor() -> None:
    def component(name: str, producer: str) -> dict:
        return {
            "component_id": name,
            "corpus_meta": {
                "a1_post_wave_audit": {
                    "source_provenance": {
                        "current": {"producer_checkpoint_sha256": producer}
                    }
                }
            },
        }

    parent = "sha256:" + "1" * 64
    predecessor = "sha256:" + "2" * 64
    descriptor = {
        "components": [
            component("n128", parent),
            component("n256", parent),
            component("predecessor_replay", predecessor),
        ]
    }
    value = turn.bind_teacher_lineage(
        descriptor,
        parent_checkpoint_sha256=parent,
        expected_predecessor_sha256=predecessor,
    )
    assert value["learner_parent_checkpoint_sha256"] == parent

    descriptor["components"][0] = component("n128", predecessor)
    with pytest.raises(turn.FlywheelTurnError, match="not generated by"):
        turn.bind_teacher_lineage(
            descriptor,
            parent_checkpoint_sha256=parent,
            expected_predecessor_sha256=predecessor,
        )


